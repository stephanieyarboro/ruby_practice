puts "what's your fave animal"
animal = gets.chomp.downcase
if animal == "cat"
    puts "meow"
elsif animal == "dog"
    puts "bark bark"
elsif animal == "frog"
puts "ribbit ribbit"
elsif animal == "bird"
puts "chirp chirp"
elsif animal == "pig"
puts "oink"
elsif animal == "snake"
puts "pssssssssssst"
elsif animal == "cow"
puts "moo"
else
puts "that's a silly animal"
end